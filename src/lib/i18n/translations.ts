// 获取翻译的辅助函数
export function getTranslation<
  S extends keyof typeof translations,
  K extends keyof typeof translations[S]['zh-CN']
>(section: S, key: K, locale: Locale): string {
  const sectionData = translations[section];
  const localeData = sectionData[locale] as Record<string, string>;
  return localeData[key as string] || (sectionData['zh-CN'] as Record<string, string>)[key as string] || String(key);
}
